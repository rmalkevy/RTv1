#include "rtv1.h"

double	min_distance(t_mod *mod)
{
	double t;

	if (mod->t0 < 0)
		mod->t0 = 2000000000;
	if (mod->t1 < 0)
		mod->t1 = 2000000000;
	t = fmin(mod->t0, mod->t1);
	return (t);
}
